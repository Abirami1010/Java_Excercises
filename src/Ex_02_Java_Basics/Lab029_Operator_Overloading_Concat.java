package Ex_02_Java_Basics;

public class Lab029_Operator_Overloading_Concat {
    public static void main(String[] args) {
        String s1="Welcome";
        String s2="Onboard";
        System.out.println(s1+s2); //Joins string  --> Concatenation

        int a=10;
        int b=20;
        System.out.println(a+b); // Math

// Behaves differently with data type
// + -> Operator overloading
    }
}
