package Ex_02_Java_Basics;

public class Lab024_Arithmetic_Operators {
    public static void main(String[] args) {
        int a=20;
        int b=3;
        float c=3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a/c);   // Rule: divided by float will give output as float
        System.out.println("a+b");
    }
}


