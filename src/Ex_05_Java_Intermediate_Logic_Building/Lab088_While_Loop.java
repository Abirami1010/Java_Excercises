package Ex_05_Java_Intermediate_Logic_Building;

public class Lab088_While_Loop {
    public static void main(String[] args) {
        int i=0;  // I - Initialization

        while (i<=10)  // C- Condition must be true to enter into loop
        {
            System.out.println(i);
            i++;  // U - Updation
        }
    }
}
//Difference between For Loop and While Loop
//For -> Initialization
//Typically included in the loop syntax.Condition -> inside

// While
//Outside,
//check condition first then do operation

