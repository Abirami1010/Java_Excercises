package Ex_01_Java_Basics;

public class Lab018_Literals {
    public static void main(String[] args) {
        int age=30;  //30 --> Variable value is Literal
        // Integral Literal

        final float pi=3.14f;
        //Float Literal
        float pi2=314f;
        //float x=10.08f;

        char value='A';  // AtoZ, atoz, !@#$%^&*()_+

        /* Below literals are not commonly used in automation, just for information*/
        boolean Java_has_JVM=true;
        //Boolean Literal

        int binary_num=0b1010;  // starts with 0b
        //Binary Literal

        int octal=0101;  //starts with 0
        //Octal Lietral

        int hexa=0x0101; //starts with 0x
        // Hexadecimal literal
        // base 16 color combinations are hex
    }
}
