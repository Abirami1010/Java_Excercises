package Ex_01_Java_Basics;

public class Lab009_Variable {
    public static void main(String[] args) {

        /* data_type Variable_name (identifier) = Variable_value (literal) */
        byte age=50;
        age=55;
        System.out.println(age);

        int b=10;
        b=10+20;
        System.out.println(b);
    }

}
