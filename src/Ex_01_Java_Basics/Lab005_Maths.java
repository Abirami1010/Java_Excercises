package Ex_01_Java_Basics;

public class Lab005_Maths {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println("2+2"); //When double quotes used integer value becomes string
        System.out.println(2*2);
        System.out.println(2-2);
        System.out.println(2/2);
        System.out.println(3/2); // Rule: It always print the integer value not decimal
    }
}
